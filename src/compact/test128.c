static
void 
bin2hex(char *s, void *p, int len) {
    int i;
    
    printf("%-10s : ", s);
    
    for (i=0; i<len; i++) { 
        printf ("%02x ", ((uint8_t*)p)[i]);
        if ((i & 15) == 15) printf("\n");
    }
}

int 
main (void) {
    int     i, equ;
    uint8_t data[16], key[AES_KEY_LEN];
    
    puts ("\n**** AES-128 ECB Test ****\n");
    
    // ecb tests
    for (i=0; i<4; i++) {
      memcpy(data, ecb_plain[i], 16);
      memcpy(key, ecb_keys[i], AES_KEY_LEN);
      
      #ifdef ASM
        uint8_t s[32];
        memcpy(s, data, 16);
        memcpy(&s[16], key, 16);
        
        aes_ecb_asm(s);
        memcpy(data, s, 16);
      #else
        aes_ecb(key, data);
      #endif
      equ = (memcmp(data, ecb_cipher[i], 16) == 0);
      
      bin2hex("key", key, 16);
      bin2hex("cipher", data, 16);
      
      printf("AES-128 ECB Test #%i : %s\n\n", 
        (i+1), equ ? "OK" : "FAILED");
    }
    
    return 0;
}
